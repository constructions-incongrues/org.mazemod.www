<h1>about</h1>
<p>
Mazemod is a web radio dedicated to <a href="http://en.wikipedia.org/wiki/Tracker">amiga &amp; tracker music culture</a> streaming various styles of computer electronic music from the <a href="http://en.wikipedia.org/wiki/Demoscene">demoscene &amp; computer art subculture</a>.

<p>
Most notorious styles which came out of the trackers are the <a href="http://en.wikipedia.org/wiki/Chiptune">chiptunes</a>, synthetic sounds with a videogame &amp; fun melody appeal,
<a href="http://en.wikipedia.org/wiki/Cracktro">demo songs</a> featured in the crack warez intros &amp; also <a href="http://en.wikipedia.org/wiki/Techno_music">acid techno</a> tracks which reached the computer art subculture in the nineties.
</p>

<p>
The radio delivers playlists as selected themes and special shows by artists from demoscene &amp; electroscene. All the tracks playlists can be downloaded as standalone zip pack with the original modules.
</p>

<p>
If you have some good tracks that could be played on the radio, don't hesitate to send us a playlist with your favorite mods in a zip format.
</p>

<p>
The radio is non commercial and non profit, all the music rights belong to the authors of these music modules, if you wish to contact us for various issues. <a href="mailto:info@mazemod.org">info@mazemod.org</a>
</p>

<p>
This project was launched in May 2008 by <a href="http://www.daheardit-records.net/">Bozoo</a> (webdesign),
